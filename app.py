def symptom_to_disease(symptom_list):
    bin_list = np.zeros(len(symptoms)).astype(int)
    for k in range(len(symptoms)):
        for z in symptom_list:
            if z == symptoms[k]:
                bin_list[k] = 1
    if np.sum(bin_list) == 0:
        return "Please select something"
    return disease[model.predict([bin_list])[0]]



